<p style="font-size: 19;"><b>Dalam acara JRO 2017, berbagai kegiatan diadakan mulai dari perlombaan balap losstang, pasar klithikan, 
pertunjukan seni onthel hingga puncaknya adalah kirab sepeda onthel berkeliling Kota Jogja 
bersama seluruh peserta yang hadir. 
Tetapi, tidak hanya kegiatan-kegiatan tersebut yang membuat acara ini menarik. 
Para onthelis yang datang membawa cerita-cerita tersendiri ke acara Jogja Republik Onthel ini, simak ceritanya.</b>

<p style="font-size: 19;"><b>1. Gowes 220 kilometer, Kebumen-Jogja-Kebumen</b>
<br>Namanya Likhri Kumis, onthelis yang berasal dari Kebumen ini menghadiri event Jogja Republik Onthel 2017 
dengan bersepeda dari tempat tinggalnya menuju Jogja. 
Berangkat pukul 4 sore hingga sampai pada pukul 12 malam, 
ia menempuh 8 jam perjalanan menggunakan onthel bersama seorang temannya.
Namun ini bukan yang pertama baginya, ia mengungkapkan,
sebelumnya sudah pernah bersepeda ke Jogja pada acara tahun sebelumnya. 
<br>Banyak pengalaman yang didapatkan, salah satu yang unik adalah Likhri pernah dikira orang “stres” 
karena bersepeda onthel dengan menggunakan seragam anak Sekolah Dasar. 
“Di event sebelumnya, waktu pulang ke Kebumen saya dan teman-teman mampir ke warung beli minum, 
ternyata penjualnya mengira saya kentir (gila- red) Mas karena berpakaian seperti ini.”
<br>Dalam acara JRO tahun ini pun, ia menggunakan seragam SD saat bersepeda berkeliling Jogja.
 Aksinya ini menjadi perhatian publik dan banyak orang ingin berfoto dengannya. </p>

 <p style="font-size: 19;"><b>2. Ngonthel Pulang Pergi Lumajang-Jogja </b>
<br style ="";>Onthelis yang lain punya cerita berbeda. Nama panggilannya Mbah Dud, berasal dari Kediri, Jawa Timur. 
Ia juga salah satu onthelis yang berangkat ke JRO dengan bersepeda PP (pulang pergi).
Ia bercerita, bersama seorang  rekannya “ngonthel” dari Lumajang ke Jogja untuk 
menghadiri gelaran yang diadakan di Benteng Vredeburg.
<br>Menempuh jarak ratusan kilometer, Mbah Dud mengatakan perlu waktu 6 hari perjalanan untuk sampai 
tujuan dari tempat berangkatnya. 
Meski sudah berumur, ia merasa senang lantaran memang hobi bersepeda onthel dari dulu. 
<br>“Ya senang saja kalau bersepeda, dari dulu. Apalagi untuk jarak jauh dengan teman-teman. 
Bisa ketemu sama orang-orang baru juga di kegiatan seperti ini,” ungkapnya.</p>

<p style="font-size: 19;"><b>3. Bersepeda Onthel Lintas Pulau </b>
<br>Nama lengkapnya Kasan Miarjo, berusia 72 tahun. 
Ia biasa dipanggil Mbah Hasan. Ia juga datang ke acara Jogja Republik Onthel dengan gowes sepedanya. 
Berangkat dari Jakarta, menempuh perjalanan selama 4 hari ia sempat singgah di kampung halamannya Purbalingga.
Kemudian melanjutkan perjalanan 2 hari hingga sampai ke Jogja.  
“Dari Jakarta ke Purbalingga, saya pulang dulu sebentar ke rumah. Terus lanjut lagi ke sini. Waktunya 4 hari sama 2 hari,” jelasnya.
<br>Mbah Hasan juga pernah mencuri perhatian masyarakat saat mudik lebaran lalu menggunakan sepeda onthel bersama 
anaknya dari Jakarta ke Purbalingga, Jawa Tengah. Selain itu, Mbah Hasan juga pernah bersepeda ke tempat-tempat lain, 
bahkan hingga ke daerah Bima, Nusa Tenggara Barat. Berangkat dari Jakarta, ia menempuh jarak 1.739 kilometer untuk sampai ke Bima. 
Perjalanannya ini diabadikan dengan papan di bagian belakang sepedanya
<br>Mbah Hasan berpesan agar anak-anak muda sekarang menjadikan sepeda sebagai alat transportasi utama, 
karena selain murah dan ramah lingkungan juga bisa mendapat pengalaman dan teman baru.

